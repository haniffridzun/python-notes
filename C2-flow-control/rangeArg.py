# starting, stopping, stepping arguments to range()

for i in range(12, 16): # 12 <= i < 16
    print(i)
'''
output:
12
13
14
15
'''

for i in range(0, 10, 2): # 0 <= i <10, increment by 2
    print(i)
'''
output:
0
2
4
6
8
'''

# count down loop
for i in range(5, -1, -1):
    print(i)
'''
output:
5
4
3
2
1
0
'''
